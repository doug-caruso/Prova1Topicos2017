
public class Result {
	private int num;
	private int fibonacci;

	public Result(int num, int fibonacci) {
		this.num = num;
		this.fibonacci = fibonacci;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getFibonacci() {
		return fibonacci;
	}

	public void setFibonacci(int fibonacci) {
		this.fibonacci = fibonacci;
	}

}
